public class Leccion_1_05_A {
	public static void main(String[] args) {
		int i = Integer.MAX_VALUE;
		System.out.println(i);
		i++;
		System.out.println(i);
		i = Integer.MIN_VALUE;
		System.out.println(i);
		i--;
		System.out.println(i);
	}
}
